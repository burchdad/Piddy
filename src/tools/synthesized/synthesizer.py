"""
ToolSynthesizer — creates new Python tool modules on the fly.

When the autonomous loop diagnoses that a required capability is missing,
this synthesizer:
1. Analyses what kind of tool is needed (from error + context)
2. Picks a template (executor, analyzer, generator, data-pipeline)
3. Generates a concrete module with the right signature
4. Writes it to src/tools/synthesized/generated/<name>.py
5. Registers it in the runtime registry so the immediate retry can use it
6. Records the synthesis event in the failure-memory DB

The generated code is deterministic (no exec/eval of user input).
Templates are filled with validated, sanitised parameters only.
"""

